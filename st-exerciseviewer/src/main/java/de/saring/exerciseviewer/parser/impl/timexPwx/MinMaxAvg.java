package de.saring.exerciseviewer.parser.impl.timexPwx;

public class MinMaxAvg {
    private float min = 0;
    private float max = 0;
    private float avg = 0;

    public void setMin(float in) {
        min = in;
    }

    public float getMin() {
        return min;
    }

    public void setMax(float in) {
        max = in;
    }

    public float getMax() {
        return max;
    }

    public void setAvg(float in) {
        avg = in;
    }

    public float getAvg() {
        return avg;
    }
}